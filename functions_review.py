def calculate_distance():
    print("Input the speed.")
    speed = int(input())
    print("Speed>", speed, "Km/h")
    
    print("Input the time.")
    time = int(input())
    print ("Time>", time, "hours.")
    
    product = speed * time
    
    print("The distance is", product, "km.")
    


calculate_distance()
    
    
def calculate_area_of_rectangle():
    print("Input the length.")
    length = int(input())
    print ("Length>", length)
    
    print("Input the width.")
    width = int(input())
    print ("Width>", width)
    
    product = length * width
    
    print("The area is", product)
    
calculate_area_of_rectangle()
    
    
